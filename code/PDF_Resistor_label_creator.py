def E_to_val(Ex, idx):
    """
    K_val = E_to_val(E6, ["K","R"])
    """
    E_out = []
    for i in idx:
        for e in Ex:
            # 100
            # 100K
            if e[2] != '0':
                oo = e[0]+e[1]+e[2]+i
                # 10K0
                ooo = e[0]+e[1]+i+e[2]
                # 10K0
                oooo = e[0]+i+e[1]+e[2]
            if e[2] == '0':
                oo = e[0]+e[1]+e[2]+i
                # 10K0
                ooo = e[0]+e[1]+i
                # 10K0
                oooo = e[0]+i+e[1]
            if e[1] == '0':
                oo = e[0]+e[1]+e[2]+i
                # 10K0
                ooo = e[0]+e[1]+i
                # 10K0
                oooo = e[0]+i
            E_out.append(oo)
            E_out.append(ooo)
            E_out.append(oooo)
        #print(i,e)
    return E_out

